// Declaraçções de funcções javascript

// function declaration
function soma(x, y){
    return x + y
}

// Função expression
const sub = function(x, y){
    return x - y 
}

// Named function expression
const mult = function mult(x, y){
    return x * y
}

/* 
    Caso uma função seja armazenada em uma variavel ou constante, a mesmo não poderá
    ser chamada antes da função, dierente da function declaration que pode ser chamada
    antes
 */